package loops;

public class MyFirstLoop {
    public static void main(String[] args) {

        int num1 = 100;
        int num2 = 155;

        while(num1<num2){      // while the condition is true it will repeat/loop running
            System.out.println("num2 is greater than num1");
            num1+=10;
        }

        System.out.println("End");

    }
}
